     &   +                 RXRAT(   288 )      ! BALP+MO2=HO2+BAL1+HCHO
     &   +                 RXRAT(   289 )      ! BAL1+MO2=HO2+BAL2+HCHO
     &   +    2.0000D+00 * RXRAT(   290 )      ! ADDC+MO2=0.2000D+01*HO2+...
     &   +                 RXRAT(   291 )      ! MCTP+MO2=HO2+MCTO+HCHO
     &   +                 RXRAT(   292 )      ! ORAP+MO2=HO2+HCHO+GLY
     &   +    2.0000D+00 * RXRAT(   293 )      ! OLNN+MO2=0.2000D+01*HO2+...
