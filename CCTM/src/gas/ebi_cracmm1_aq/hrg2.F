     &   +    4.8000D-01 * RXRAT(   375 )      ! LIMP2+APIP1=0.4800D+00*HO+...
     &   +    4.8000D-01 * RXRAT(   376 )      ! LIMP2+LIMP1=0.4800D+00*HO+...
     &   +    4.8000D-01 * RXRAT(   377 )      ! LIMP2+ISOP=0.4800D+00*HO+...
     &   +    2.8000D-01 * RXRAT(   388 )      ! ACRO+O3=0.2800D+00*HO+...
     &   +    2.0400D-01 * RXRAT(   390 )      ! ACRO=0.2040D+00*HO+0.4770D+...
     &   +    8.0000D-02 * RXRAT(   397 )      ! BDE13+O3=0.8000D-01*HO+...
     &   +    2.0000D-02 * RXRAT(   403 )      ! FURAN+O3=0.2000D-01*HO+ALD

      P4 = YC0( HO ) + P4 * DTC

