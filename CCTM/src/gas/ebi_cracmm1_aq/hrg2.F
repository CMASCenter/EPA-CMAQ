     &   +    8.4000D-01 * RXRAT(   141 )      ! LIM+O3=0.8400D+00*HO+...
     &   +    4.0000D-02 * RXRAT(   142 )      ! LIMAL+O3=0.4000D-01*HO+...
     &   +    1.9000D-01 * RXRAT(   144 )      ! MACR+O3=0.1900D+00*HO+...
     &   +    1.6000D-01 * RXRAT(   145 )      ! MVK+O3=0.1600D+00*HO+...
     &   +    1.0000D-01 * RXRAT(   146 )      ! UALD+O3=0.1000D+00*HO+...
     &   +    5.0000D-02 * RXRAT(   147 )      ! DCB1+O3=0.5000D-01*HO+HO2+...
     &   +    5.0000D-02 * RXRAT(   148 )      ! DCB2+O3=0.5000D-01*HO+HO2+...
     &   +    5.0000D-02 * RXRAT(   149 )      ! DCB3+O3=0.5000D-01*HO+HO2+...
     &   +    8.2000D-01 * RXRAT(   190 )      ! APIP2+NO=0.8200D+00*HO+...
     &   +    8.2000D-01 * RXRAT(   192 )      ! APINP2+NO=0.8200D+00*HO+...
     &   +    7.7000D-01 * RXRAT(   194 )      ! LIMP2+NO=0.7700D+00*HO+...
     &   +    7.7000D-01 * RXRAT(   196 )      ! LIMNP2+NO=0.7700D+00*HO+...
     &   +    5.0000D-01 * RXRAT(   309 )      ! APIP2+ACO3=0.5000D+00*HO+...
     &   +    5.0000D-01 * RXRAT(   313 )      ! LIMP2+ACO3=0.5000D+00*HO+...
