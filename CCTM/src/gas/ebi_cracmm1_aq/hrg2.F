     &   +    7.7000D-01 * RXRAT(   193 )      ! LIMP1+NO=0.7700D+00*HO2+...
     &   +    9.5000D-01 * RXRAT(   197 )      ! PINALP+NO=0.9500D+00*HO2+...
     &   +    9.4000D-01 * RXRAT(   198 )      ! LIMALP+NO=0.9400D+00*HO2+...
     &   +    6.7000D-01 * RXRAT(   202 )      ! MEKP+NO=0.6700D+00*HO2+NO2+...
     &   +    7.7000D-01 * RXRAT(   203 )      ! KETP+NO=0.7700D+00*HO2+...
     &   +    5.0000D-01 * RXRAT(   205 )      ! MCP+NO=0.5000D+00*HO2+NO2+...
     &   +    3.0000D-01 * RXRAT(   206 )      ! MVKP+NO=0.3000D+00*HO2+...
     &   +                 RXRAT(   207 )      ! UALP+NO=HO2+NO2+0.6100D+...
     &   +                 RXRAT(   210 )      ! ADDC+NO=HO2+NO2+0.3200D+...
     &   +                 RXRAT(   212 )      ! ORAP+NO=HO2+GLY+NO2
     &   +                 RXRAT(   213 )      ! OLNN+NO=HO2+NO2+ONIT
     &   +    7.4000D-01 * RXRAT(   259 )      ! MO2+MO2=0.7400D+00*HO2+...
     &   +                 RXRAT(   260 )      ! ETHP+MO2=HO2+0.7500D+...
     &   +    8.9400D-01 * RXRAT(   261 )      ! HC3P+MO2=0.8940D+00*HO2+...
