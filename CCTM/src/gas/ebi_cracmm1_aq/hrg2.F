     &   +    3.4000D-01 * RXRAT(    22 )      ! MACR=0.3400D+00*HO+0.6600D+...
     &   +                 RXRAT(    33 )      ! OP1=HO+HO2+HCHO
     &   +                 RXRAT(    34 )      ! OP2=HO+HO2+ALD
     &   +                 RXRAT(    35 )      ! OPB=HO+HO2+ALD
     &   +                 RXRAT(    36 )      ! OP3=HO+HO2+ALD
     &   +                 RXRAT(    37 )      ! PAA=HO+MO2
     &   +   OH_S  *       RXRAT(     2 )      ! O1D=0.2000D+01*HO
     &   +    8.0000D-02 * RXRAT(   136 )      ! ETE+O3=0.8000D-01*HO+...
     &   +    2.2000D-01 * RXRAT(   137 )      ! OLT+O3=0.2200D+00*HO+...
     &   +    4.6000D-01 * RXRAT(   138 )      ! OLI+O3=0.4600D+00*HO+...
     &   +    2.5000D-01 * RXRAT(   139 )      ! ISO+O3=0.2500D+00*HO+...
