     &   +    5.0000D-01 * RXRAT(   281 )      ! ACTP+MO2=0.5000D+00*HO2+...
     &   +    8.3400D-01 * RXRAT(   282 )      ! MEKP+MO2=0.8340D+00*HO2+...
     &   +                 RXRAT(   283 )      ! KETP+MO2=HO2+0.7500D+...
     &   +    5.0000D-01 * RXRAT(   284 )      ! MACP+MO2=0.5000D+00*HO2+...
     &   +                 RXRAT(   285 )      ! MCP+MO2=HO2+NO2+0.1500D+...
