     &     +                 RKI(    78 ) * YC ( SO2         )   ! HO+SO2=HO2+SULF+...
     &     +                 RKI(    79 ) * YC ( CO          )   ! HO+CO=HO2
     &     +    3.5000D-01 * RKI(    87 ) * YC ( ACE         )   ! HO+ACE=0.3500D+...
     &     +    5.3000D-01 * RKI(    88 ) * YC ( BEN         )   ! HO+BEN=0.5300D+...
     &     +    1.8000D-01 * RKI(    89 ) * YC ( TOL         )   ! HO+TOL=0.1800D+...
