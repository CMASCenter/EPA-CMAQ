      SUBROUTINE HRG2( DTC )
C**********************************************************************
C
C  FUNCTION: To solve for the concentration of HO, HO2, HONO, and
C            HNO4 alebraically.
C
C  PRECONDITIONS: For the CRACMM1_AQ mechanism
C
C  KEY SUBROUTINES/FUNCTIONS CALLED: None
C
C  REVISION HISTORY: Created by EBI solver program, Sep 28, 2021
C
C   18 Jul 14 B.Hutzell: revised to use real(8) variables
C**********************************************************************
      USE HRDATA

      IMPLICIT NONE


C..INCLUDES: None


C..ARGUMENTS:
      REAL( 8 ), INTENT( IN ) :: DTC                      ! Time step

C..PARAMETERS: None


C..EXTERNAL FUNCTIONS: NONE


C..SAVED LOCAL VARIABLES:
!     CHARACTER( 16 ), SAVE :: PNAME = 'HRG2'             ! Program name


C..SCRATCH LOCAL VARIABLES:
      REAL( 8 ) ::    O1D_S                 ! sum of O1D loss frequencies
      REAL( 8 ) ::    OH_S                  ! stoich coeff for OH from O1D+H2O or H2
      REAL( 8 ) ::    HO2_S                 ! stoich coeff for HO2 from O1D+H2 rxn
      REAL( 8 ) ::    XOH_S                 ! stoich coeff for OH & HONO from NO2EX loss rxn
      REAL( 8 ) ::    R4_19                 ! production of OH from HONO
      REAL( 8 ) ::    R19_4                 ! production of HONO from OH
      REAL( 8 ) ::    R4_5                  ! production of OH from HO2
      REAL( 8 ) ::    R5_4                  ! production of HO2 from OH
      REAL( 8 ) ::    R5_21                 ! production of HO2 from HNO4
      REAL( 8 ) ::    R21_5                 ! production of HNO4 from HO2
      REAL( 8 ) ::    P4, P5, P19, P21      ! species production form other paths
      REAL( 8 ) ::    L4, L5, L19, L21      ! species loss terms

      REAL( 8 ) ::    A, B, C               ! coeffs of quadratic eq. for HO2
      REAL( 8 ) ::    Q                     ! intermediate term

      REAL( 8 ) ::    T1, T2, T3            ! intermediate terms

      REAL( 8 ) ::    L21_INV               ! reciprocal of HNO4 loss term

C**********************************************************************


c..stoichiometric coefficient for production of HOX from O3 via O1D
      O1D_S =                 RKI(    40 )                     ! O1D=O3P
     &      +                 RKI(    41 )                     ! O1D=O3P
     &      +                 RKI(    42 )                     ! O1D=0.2000D+01*HO

      OH_S =    2.0000D+00 * RKI(    42 )                     ! O1D=0.2000D+01*HO

      OH_S  = OH_S / O1D_S


c..Production of HO from HO2 (r4,5 terms )
      R4_5 =                 RKI(    35 ) * YC ( O3       )   ! HO2+O3=HO
     &     +                 RKI(    50 ) * YC ( NO       )   ! HO2+NO=HO+NO2
     &     +    7.0000D-01 * RKI(    59 ) * YC ( NO3      )   ! HO2+NO3=0.7000D+...
     &     +    4.4000D-01 * RKI(   227 ) * YC ( ACO3     )   ! HO2+ACO3=0.4400D+...
     &     +    4.4000D-01 * RKI(   228 ) * YC ( RCO3     )   ! HO2+RCO3=0.4400D+...
     &     +    1.5000D-01 * RKI(   229 ) * YC ( ACTP     )   ! HO2+ACTP=0.1500D+...

      R4_5  = R4_5 * DTC

c..Production of HO from HONO (r4,19 terms )
      R4_19 =                 RKI(     7 )                     ! HONO=HO+NO

      R4_19 = R4_19 * DTC

c..Remaining HO production
      P4 =    2.0000D+00 * RXRAT(     3 )      ! H2O2=0.2000D+01*HO
     &   +                 RXRAT(     8 )      ! HNO3=HO+NO2
     &   +    2.0000D-01 * RXRAT(     9 )      ! HNO4=0.2000D+00*HO+0.8000D+...
     &   +    3.4000D-01 * RXRAT(    19 )      ! MACR=0.3400D+00*HO+0.6600D+...
     &   +                 RXRAT(    28 )      ! OP1=HO+HO2+HCHO
     &   +                 RXRAT(    29 )      ! OP2=HO+HO2+ALD
     &   +                 RXRAT(    30 )      ! PAA=HO+MO2
     &   +   OH_S  *       RXRAT(     2 )      ! O1D=0.2000D+01*HO
     &   +    8.0000D-02 * RXRAT(   124 )      ! ETE+O3=0.8000D-01*HO+...
     &   +    2.2000D-01 * RXRAT(   125 )      ! OLT+O3=0.2200D+00*HO+...
     &   +    4.6000D-01 * RXRAT(   126 )      ! OLI+O3=0.4600D+00*HO+...
     &   +    2.5000D-01 * RXRAT(   127 )      ! ISO+O3=0.2500D+00*HO+...
     &   +    8.5000D-01 * RXRAT(   128 )      ! API+O3=0.8500D+00*HO+...
     &   +    8.5000D-01 * RXRAT(   129 )      ! LIM+O3=0.8500D+00*HO+...
     &   +    1.9000D-01 * RXRAT(   130 )      ! MACR+O3=0.1900D+00*HO+...
     &   +    1.6000D-01 * RXRAT(   131 )      ! MVK+O3=0.1600D+00*HO+...
     &   +    1.0000D-01 * RXRAT(   132 )      ! UALD+O3=0.1000D+00*HO+...
     &   +    5.0000D-02 * RXRAT(   133 )      ! DCB1+O3=0.5000D-01*HO+HO2+...
     &   +    5.0000D-02 * RXRAT(   134 )      ! DCB2+O3=0.5000D-01*HO+HO2+...
     &   +    5.0000D-02 * RXRAT(   135 )      ! DCB3+O3=0.5000D-01*HO+HO2+...
     &   +    5.0000D-02 * RXRAT(   136 )      ! EPX+O3=0.5000D-01*HO+...
     &   +    5.0000D-01 * RXRAT(   153 )      ! EPX+NO3=0.5000D+00*HO+...
     &   +    2.8000D-01 * RXRAT(   156 )      ! TR2=0.2800D+00*HO+0.2900D+...
     &   +    4.9000D-01 * RXRAT(   157 )      ! TOLP=0.4900D+00*HO+0.1000D-...
     &   +    1.5800D-01 * RXRAT(   158 )      ! XY2=0.1580D+00*HO+0.3080D+...
     &   +    3.9000D-01 * RXRAT(   159 )      ! XYLP=0.3900D+00*HO+0.1000D-...
     &   +    1.5800D-01 * RXRAT(   160 )      ! XYO2=0.1580D+00*HO+0.3080D+...
     &   +    3.9000D-01 * RXRAT(   161 )      ! XYOP=0.3900D+00*HO+0.1000D-...

      P4 = YC0( HO ) + P4 * DTC

c..Production of HO2 from OH ( r5,4 terms )
      R5_4 =                 RKI(    34 ) * YC ( O3       )   ! HO+O3=HO2
     &     +                 RKI(    43 )                     ! HO=HO2
     &     +                 RKI(    47 ) * YC ( H2O2     )   ! HO+H2O2=HO2
     &     +                 RKI(    58 ) * YC ( NO3      )   ! HO+NO3=HO2+NO2
     &     +                 RKI(    69 ) * YC ( SO2      )   ! HO+SO2=HO2+SULF+...
     &     +                 RKI(    70 ) * YC ( CO       )   ! HO+CO=HO2
     &     +    4.9000D-02 * RKI(    75 ) * YC ( HC8      )   ! HO+HC8=0.4900D-...
     &     +    3.5000D-01 * RKI(    79 ) * YC ( ACE      )   ! HO+ACE=0.3500D+...
     &     +    6.4800D-01 * RKI(    80 ) * YC ( BEN      )   ! HO+BEN=0.6480D+...
     &     +    1.7700D-01 * RKI(    81 ) * YC ( TOL      )   ! HO+TOL=0.1770D+...
     &     +    1.7700D-01 * RKI(    82 ) * YC ( XYM      )   ! HO+XYM=0.1770D+...
     &     +    1.7700D-01 * RKI(    83 ) * YC ( XYE      )   ! HO+XYE=0.1770D+...
     &     +                 RKI(    87 ) * YC ( HCHO     )   ! HO+HCHO=HO2+CO
     &     +                 RKI(    93 ) * YC ( HKET     )   ! HO+HKET=HO2+MGLY
     &     +                 RKI(    97 ) * YC ( GLY      )   ! HO+GLY=HO2+...
     &     +    5.2000D-01 * RKI(    99 ) * YC ( DCB1     )   ! HO+DCB1=0.5200D+...
     &     +    5.2000D-01 * RKI(   100 ) * YC ( DCB2     )   ! HO+DCB2=0.5200D+...
     &     +    5.6000D-01 * RKI(   101 ) * YC ( DCB3     )   ! HO+DCB3=0.5600D+...
     &     +    7.3000D-01 * RKI(   103 ) * YC ( PHEN     )   ! HO+PHEN=0.7300D+...
     &     +    7.3000D-01 * RKI(   104 ) * YC ( CSL      )   ! HO+CSL=0.7300D+...
     &     +                 RKI(   105 ) * YC ( EPX      )   ! HO+EPX=HO2+XO2+...
     &     +                 RKI(   107 ) * YC ( MOH      )   ! HO+MOH=HO2+HCHO
     &     +                 RKI(   108 ) * YC ( EOH      )   ! HO+EOH=HO2+ACD
     &     +                 RKI(   109 ) * YC ( ROH      )   ! HO+ROH=HO2+...
     &     +                 RKI(   110 ) * YC ( ETEG     )   ! HO+ETEG=HO2+ALD
     &     +                 RKI(   115 ) * YC ( ORA1     )   ! HO+ORA1=HO2
     &     +    4.9000D-02 * RKI(   371 ) * YC ( ROCIOXY  )   ! HO+ROCIOXY=...
     &     +    1.7700D-01 * RKI(   382 ) * YC ( FURAN    )   ! HO+FURAN=0.1770D+...
     &     +                 RKI(   383 ) * YC ( PROG     )   ! HO+PROG=HO2+...

      R5_4  = R5_4 * DTC

c..Production of HO2 from HNO4 (r5,21 term )
      R5_21 =    8.0000D-01 * RKI(     9 )                     ! HNO4=0.8000D+...
     &      +                 RKI(    67 )                     ! HNO4=HO2+NO2

      R5_21 = R5_21 * DTC

c..Remaining HO2 production terms
      P5 =    2.0000D+00 * RXRAT(    11 )      ! HCHO=0.2000D+01*HO2+CO
     &   +                 RXRAT(    12 )      ! ACD=HO2+MO2+CO
     &   +                 RXRAT(    13 )      ! ALD=HO2+ETHP+CO
     &   +    1.2200D+00 * RXRAT(    15 )      ! UALD=0.1220D+01*HO2+...
     &   +                 RXRAT(    18 )      ! HKET=HO2+ACO3+HCHO
     &   +    6.6000D-01 * RXRAT(    19 )      ! MACR=0.6600D+00*HO2+...
     &   +    2.0000D+00 * RXRAT(    23 )      ! GLY=0.2000D+01*HO2+0.2000D+...
     &   +                 RXRAT(    24 )      ! MGLY=HO2+ACO3+CO
     &   +    1.5000D+00 * RXRAT(    25 )      ! DCB1=0.1500D+01*HO2+...
     &   +    1.5000D+00 * RXRAT(    26 )      ! DCB2=0.1500D+01*HO2+...
     &   +                 RXRAT(    27 )      ! BALD=HO2+CHO+CO
     &   +                 RXRAT(    28 )      ! OP1=HO2+HO+HCHO
     &   +                 RXRAT(    29 )      ! OP2=HO2+HO+ALD
     &   +                 RXRAT(    31 )      ! ONIT=HO2+NO2+0.2000D+...
     &   +    1.5000D-01 * RXRAT(   124 )      ! ETE+O3=0.1500D+00*HO2+...
     &   +    3.2000D-01 * RXRAT(   125 )      ! OLT+O3=0.3200D+00*HO2+...
     &   +    7.0000D-02 * RXRAT(   126 )      ! OLI+O3=0.7000D-01*HO2+...
     &   +    2.5000D-01 * RXRAT(   127 )      ! ISO+O3=0.2500D+00*HO2+...
     &   +    1.0000D-01 * RXRAT(   128 )      ! API+O3=0.1000D+00*HO2+...
     &   +    1.0000D-01 * RXRAT(   129 )      ! LIM+O3=0.1000D+00*HO2+...
     &   +    1.4000D-01 * RXRAT(   130 )      ! MACR+O3=0.1400D+00*HO2+...
     &   +    1.1000D-01 * RXRAT(   131 )      ! MVK+O3=0.1100D+00*HO2+...
     &   +    7.2000D-02 * RXRAT(   132 )      ! UALD+O3=0.7200D-01*HO2+...
     &   +                 RXRAT(   133 )      ! DCB1+O3=HO2+0.5000D-01*HO+...
     &   +                 RXRAT(   134 )      ! DCB2+O3=HO2+0.5000D-01*HO+...
     &   +                 RXRAT(   135 )      ! DCB3+O3=HO2+0.5000D-01*HO+...
     &   +    1.5000D+00 * RXRAT(   136 )      ! EPX+O3=0.1500D+01*HO2+...
     &   +                 RXRAT(   144 )      ! HCHO+NO3=HO2+CO+HNO3
     &   +                 RXRAT(   148 )      ! UALD+NO3=HO2+XO2+0.6680D+...
     &   +                 RXRAT(   149 )      ! GLY+NO3=HO2+0.2000D+01*CO+HNO3
     &   +    1.5000D+00 * RXRAT(   153 )      ! EPX+NO3=0.1500D+01*HO2+...
     &   +    2.9000D-01 * RXRAT(   156 )      ! TR2=0.2900D+00*HO2+0.2800D+...
     &   +    1.0000D-02 * RXRAT(   157 )      ! TOLP=0.1000D-01*HO2+...
     &   +    3.0800D-01 * RXRAT(   158 )      ! XY2=0.3080D+00*HO2+0.1580D+...
     &   +    1.0000D-02 * RXRAT(   159 )      ! XYLP=0.1000D-01*HO2+...
     &   +    3.0800D-01 * RXRAT(   160 )      ! XYO2=0.3080D+00*HO2+...
     &   +    1.0000D-02 * RXRAT(   161 )      ! XYOP=0.1000D-01*HO2+...
     &   +                 RXRAT(   168 )      ! MO2+NO=HO2+NO2+HCHO
     &   +                 RXRAT(   169 )      ! ETHP+NO=HO2+NO2+ACD
     &   +    6.6000D-01 * RXRAT(   170 )      ! HC3P+NO=0.6600D+00*HO2+...
     &   +    2.0000D-01 * RXRAT(   171 )      ! HC5P+NO=0.2000D+00*HO2+...
     &   +    6.0600D-01 * RXRAT(   172 )      ! HC8P+NO=0.6060D+00*HO2+...
     &   +                 RXRAT(   173 )      ! ETEP+NO=HO2+NO2+0.1600D+...
     &   +    7.8000D-01 * RXRAT(   174 )      ! OLTP+NO=0.7800D+00*HO2+...
     &   +    8.3000D-01 * RXRAT(   175 )      ! OLIP+NO=0.8300D+00*HO2+...
     &   +    9.1800D-01 * RXRAT(   176 )      ! BENP+NO=0.9180D+00*HO2+...
     &   +    9.5000D-01 * RXRAT(   178 )      ! TOLP+NO=0.9500D+00*HO2+...
     &   +    5.0000D-01 * RXRAT(   179 )      ! PER1+NO=0.5000D+00*HO2+...
     &   +    9.5000D-01 * RXRAT(   181 )      ! XYLP+NO=0.9500D+00*HO2+...
     &   +    9.5000D-01 * RXRAT(   182 )      ! PER2+NO=0.9500D+00*HO2+...
     &   +    9.5000D-01 * RXRAT(   183 )      ! XYOP+NO=0.9500D+00*HO2+...
     &   +    8.8000D-01 * RXRAT(   184 )      ! ISOP+NO=0.8800D+00*HO2+...
     &   +    8.2000D-01 * RXRAT(   185 )      ! APIP+NO=0.8200D+00*HO2+...
     &   +                 RXRAT(   186 )      ! LIMP+NO=HO2+0.6800D+...
     &   +    6.7000D-01 * RXRAT(   190 )      ! MEKP+NO=0.6700D+00*HO2+NO2+...
     &   +    7.7000D-01 * RXRAT(   191 )      ! KETP+NO=0.7700D+00*HO2+...
     &   +    5.0000D-01 * RXRAT(   193 )      ! MCP+NO=0.5000D+00*HO2+NO2+...
     &   +    3.0000D-01 * RXRAT(   194 )      ! MVKP+NO=0.3000D+00*HO2+...
     &   +                 RXRAT(   195 )      ! UALP+NO=HO2+NO2+0.6100D+...
     &   +                 RXRAT(   198 )      ! ADDC+NO=HO2+NO2+0.3200D+...
     &   +                 RXRAT(   200 )      ! ORAP+NO=HO2+GLY+NO2
     &   +                 RXRAT(   201 )      ! OLNN+NO=HO2+NO2+ONIT
     &   +    7.4000D-01 * RXRAT(   244 )      ! MO2+MO2=0.7400D+00*HO2+...
     &   +                 RXRAT(   245 )      ! ETHP+MO2=HO2+0.7500D+...
     &   +    8.9400D-01 * RXRAT(   246 )      ! HC3P+MO2=0.8940D+00*HO2+...
     &   +    8.4200D-01 * RXRAT(   247 )      ! HC5P+MO2=0.8420D+00*HO2+...
     &   +    9.1000D-01 * RXRAT(   248 )      ! HC8P+MO2=0.9100D+00*HO2+...
     &   +                 RXRAT(   249 )      ! ETEP+MO2=HO2+0.1950D+...
     &   +                 RXRAT(   250 )      ! OLTP+MO2=HO2+0.1500D+...
     &   +                 RXRAT(   251 )      ! OLIP+MO2=HO2+0.7500D+...
     &   +    1.6000D+00 * RXRAT(   252 )      ! BENP+MO2=0.1600D+01*HO2+...
     &   +                 RXRAT(   253 )      ! TLP1+MO2=HO2+HCHO+BALD
     &   +    2.0000D+00 * RXRAT(   254 )      ! TOLP+MO2=0.2000D+01*HO2+...
     &   +    2.0000D+00 * RXRAT(   255 )      ! PER1+MO2=0.2000D+01*HO2+...
     &   +                 RXRAT(   256 )      ! XYL1+MO2=HO2+HCHO+BALD
     &   +    2.0000D+00 * RXRAT(   257 )      ! XYLP+MO2=0.2000D+01*HO2+...
     &   +    2.0000D+00 * RXRAT(   258 )      ! PER2+MO2=0.2000D+01*HO2+...
     &   +    2.0000D+00 * RXRAT(   259 )      ! XYOP+MO2=0.2000D+01*HO2+...
     &   +                 RXRAT(   260 )      ! ISOP+MO2=HO2+0.1310D+...
     &   +                 RXRAT(   261 )      ! APIP+MO2=HO2+0.7500D+...
     &   +                 RXRAT(   262 )      ! LIMP+MO2=HO2+0.1040D+...
     &   +    9.0000D-01 * RXRAT(   263 )      ! ACO3+MO2=0.9000D+00*HO2+...
     &   +    9.0000D-01 * RXRAT(   264 )      ! RCO3+MO2=0.9000D+00*HO2+...
     &   +    5.0000D-01 * RXRAT(   265 )      ! ACTP+MO2=0.5000D+00*HO2+...
     &   +    8.3400D-01 * RXRAT(   266 )      ! MEKP+MO2=0.8340D+00*HO2+...
     &   +                 RXRAT(   267 )      ! KETP+MO2=HO2+0.7500D+...
     &   +    5.0000D-01 * RXRAT(   268 )      ! MACP+MO2=0.5000D+00*HO2+...
     &   +                 RXRAT(   269 )      ! MCP+MO2=HO2+NO2+0.1500D+...
     &   +                 RXRAT(   270 )      ! MVKP+MO2=HO2+0.1160D+...
     &   +                 RXRAT(   271 )      ! UALP+MO2=HO2+0.3050D+00*CO+...
     &   +                 RXRAT(   272 )      ! BALP+MO2=HO2+BAL1+HCHO
     &   +                 RXRAT(   273 )      ! BAL1+MO2=HO2+BAL2+HCHO
     &   +    2.0000D+00 * RXRAT(   274 )      ! ADDC+MO2=0.2000D+01*HO2+...
     &   +                 RXRAT(   275 )      ! MCTP+MO2=HO2+MCTO+HCHO
     &   +                 RXRAT(   276 )      ! ORAP+MO2=HO2+HCHO+GLY
     &   +    2.0000D+00 * RXRAT(   277 )      ! OLNN+MO2=0.2000D+01*HO2+...
     &   +    5.0000D-01 * RXRAT(   278 )      ! OLND+MO2=0.5000D+00*HO2+...
     &   +                 RXRAT(   279 )      ! ADCN+MO2=HO2+0.7000D+...
     &   +                 RXRAT(   280 )      ! XO2+MO2=HO2+HCHO
     &   +    5.0000D-01 * RXRAT(   281 )      ! ETHP+ACO3=0.5000D+00*HO2+...
     &   +    3.9400D-01 * RXRAT(   282 )      ! HC3P+ACO3=0.3940D+00*HO2+...
     &   +    3.4200D-01 * RXRAT(   283 )      ! HC5P+ACO3=0.3420D+00*HO2+...
     &   +    3.0300D-01 * RXRAT(   284 )      ! HC8P+ACO3=0.3030D+00*HO2+...
     &   +    5.0000D-01 * RXRAT(   285 )      ! ETEP+ACO3=0.5000D+00*HO2+...
     &   +    5.0000D-01 * RXRAT(   286 )      ! OLTP+ACO3=0.5000D+00*HO2+...
     &   +    5.0000D-01 * RXRAT(   287 )      ! OLIP+ACO3=0.5000D+00*HO2+...
     &   +    6.0000D-01 * RXRAT(   288 )      ! BENP+ACO3=0.6000D+00*HO2+...
     &   +                 RXRAT(   290 )      ! TOLP+ACO3=HO2+MO2+DCB2
     &   +                 RXRAT(   291 )      ! PER1+ACO3=HO2+MO2+MGLY+DCB1
     &   +                 RXRAT(   293 )      ! XYLP+ACO3=HO2+MO2+DCB2
     &   +                 RXRAT(   294 )      ! PER2+ACO3=HO2+MO2+MGLY+...
     &   +                 RXRAT(   295 )      ! XYOP+ACO3=HO2+MO2+0.3680D+...
     &   +    5.0000D-01 * RXRAT(   296 )      ! ISOP+ACO3=0.5000D+00*HO2+...
     &   +    5.0000D-01 * RXRAT(   297 )      ! APIP+ACO3=0.5000D+00*HO2+...
