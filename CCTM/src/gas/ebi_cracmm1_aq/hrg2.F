     &     +                 RKI(   118 ) * YC ( ROH         )   ! HO+ROH=HO2+...
     &     +                 RKI(   119 ) * YC ( ETEG        )   ! HO+ETEG=HO2+ALD
     &     +                 RKI(   126 ) * YC ( ORA1        )   ! HO+ORA1=HO2
     &     +    3.3300D-01 * RKI(   391 ) * YC ( BDE13       )   ! HO+BDE13=0.3330D+...
     &     +    4.9000D-01 * RKI(   399 ) * YC ( FURAN       )   ! HO+FURAN=0.4900D+...
     &     +    6.6000D-01 * RKI(   402 ) * YC ( FURANONE    )   ! HO+FURANONE=...
     &     +                 RKI(   405 ) * YC ( PROG        )   ! HO+PROG=HO2+...
     &     +    1.6000D-01 * RKI(   479 ) * YC ( VROCP6ARO   )   ! HO+VROCP6ARO=...
     &     +    1.6000D-01 * RKI(   485 ) * YC ( VROCP5ARO   )   ! HO+VROCP5ARO=...
     &     +    1.6000D-01 * RKI(   491 ) * YC ( NAPH        )   ! HO+NAPH=0.1600D+...

      R5_4  = R5_4 * DTC

c..Production of HO2 from HNO4 (r5,21 term )
      R5_21 =    8.0000D-01 * RKI(     9 )                        ! HNO4=0.8000D+...
     &      +                 RKI(    76 )                        ! HNO4=HO2+NO2

      R5_21 = R5_21 * DTC

c..Remaining HO2 production terms
      P5 =    2.0000D+00 * RXRAT(    11 )      ! HCHO=0.2000D+01*HO2+CO
     &   +                 RXRAT(    12 )      ! ACD=HO2+MO2+CO
     &   +                 RXRAT(    13 )      ! ALD=HO2+ETHP+CO
     &   +    1.2200D+00 * RXRAT(    16 )      ! UALD=0.1220D+01*HO2+...
     &   +                 RXRAT(    17 )      ! PINAL=HO2+HC10P+CO
     &   +                 RXRAT(    18 )      ! LIMAL=HO2+HC10P+CO
     &   +                 RXRAT(    21 )      ! HKET=HO2+ACO3+HCHO
     &   +    6.6000D-01 * RXRAT(    22 )      ! MACR=0.6600D+00*HO2+...
     &   +    2.0000D+00 * RXRAT(    26 )      ! GLY=0.2000D+01*HO2+0.2000D+...
     &   +                 RXRAT(    27 )      ! MGLY=HO2+ACO3+CO
     &   +    1.5000D+00 * RXRAT(    28 )      ! DCB1=0.1500D+01*HO2+...
     &   +    1.5000D+00 * RXRAT(    29 )      ! DCB2=0.1500D+01*HO2+...
     &   +                 RXRAT(    31 )      ! BALD=HO2+CO+BENP
     &   +                 RXRAT(    33 )      ! OP1=HO2+HO+HCHO
     &   +                 RXRAT(    34 )      ! OP2=HO2+HO+ALD
     &   +                 RXRAT(    35 )      ! OPB=HO2+HO+ALD
     &   +                 RXRAT(    36 )      ! OP3=HO2+HO+ALD
     &   +                 RXRAT(    38 )      ! ONIT=HO2+NO2+0.2000D+...
     &   +    1.5000D-01 * RXRAT(   136 )      ! ETE+O3=0.1500D+00*HO2+...
     &   +    3.2000D-01 * RXRAT(   137 )      ! OLT+O3=0.3200D+00*HO2+...
     &   +    7.0000D-02 * RXRAT(   138 )      ! OLI+O3=0.7000D-01*HO2+...
     &   +    2.5000D-01 * RXRAT(   139 )      ! ISO+O3=0.2500D+00*HO2+...
     &   +    4.0000D-02 * RXRAT(   142 )      ! LIMAL+O3=0.4000D-01*HO2+...
     &   +    1.4000D-01 * RXRAT(   144 )      ! MACR+O3=0.1400D+00*HO2+...
     &   +    1.1000D-01 * RXRAT(   145 )      ! MVK+O3=0.1100D+00*HO2+...
     &   +    7.2000D-02 * RXRAT(   146 )      ! UALD+O3=0.7200D-01*HO2+...
     &   +                 RXRAT(   147 )      ! DCB1+O3=HO2+0.5000D-01*HO+...
     &   +                 RXRAT(   148 )      ! DCB2+O3=HO2+0.5000D-01*HO+...
     &   +                 RXRAT(   149 )      ! DCB3+O3=HO2+0.5000D-01*HO+...
     &   +                 RXRAT(   158 )      ! HCHO+NO3=HO2+CO+HNO3
     &   +                 RXRAT(   162 )      ! UALD+NO3=HO2+XO2+0.6680D+...
     &   +                 RXRAT(   163 )      ! GLY+NO3=HO2+0.2000D+01*CO+HNO3
     &   +                 RXRAT(   177 )      ! MO2+NO=HO2+NO2+HCHO
     &   +                 RXRAT(   178 )      ! ETHP+NO=HO2+NO2+ACD
     &   +    6.6000D-01 * RXRAT(   179 )      ! HC3P+NO=0.6600D+00*HO2+...
     &   +    2.0000D-01 * RXRAT(   180 )      ! HC5P+NO=0.2000D+00*HO2+...
     &   +                 RXRAT(   181 )      ! ETEP+NO=HO2+NO2+0.1600D+...
     &   +    7.8000D-01 * RXRAT(   182 )      ! OLTP+NO=0.7800D+00*HO2+...
     &   +    8.3000D-01 * RXRAT(   183 )      ! OLIP+NO=0.8300D+00*HO2+...
     &   +    9.9800D-01 * RXRAT(   184 )      ! BENP+NO=0.9980D+00*HO2+...
